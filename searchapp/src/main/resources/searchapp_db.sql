DROP DATABASE IF EXISTS searchapp;
DROP USER IF EXISTS searchappp_user;

CREATE USER searchappp_user IDENTIFIED BY 'dbuser123';
CREATE DATABASE searchapp CHARACTER SET utf8;
GRANT ALL PRIVILEGES ON searchapp.* TO searchappp_user WITH GRANT OPTION;

USE searchapp;

-- TABLES --
CREATE TABLE topic (
	id BIGINT NOT NULL AUTO_INCREMENT PRIMARY KEY,
	name VARCHAR(80) NULL DEFAULT NULL,
	description VARCHAR(5000) NULL DEFAULT NULL,
	text_field1 VARCHAR(5000) NULL DEFAULT NULL,
	text_field2 VARCHAR(5000) NULL DEFAULT NULL,
    created TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
)
ENGINE=InnoDB;